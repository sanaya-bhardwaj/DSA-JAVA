package Basics;

public class incrementdecrement {

	public static void main(String[] args) {
		int x=5;
		System.out.println(x);
		x++; //incrementing x by 1
		//x++ means x=x+1
		System.out.println(x);
		x--; //x-- means x=x-1
		System.out.println(x);
		++x; //it also means x=x+1
		System.out.println(x);
		--x; //it also means x=x-1
		System.out.println(x);
	}

}
